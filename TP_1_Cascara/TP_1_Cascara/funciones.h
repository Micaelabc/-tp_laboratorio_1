#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED
#endif // FUNCIONES_H_INCLUDED

/** \brief suma de dos numeros (a , b)
 *
 * \param el primer numero a sumar (float a)
 * \param el segundo numero a sumar (float b)
 * \return resultado de la suma
 *
 */

float Suma (float a, float b);

/** \brief resta de dos numeros (a , b)
 *
 * \param el primer numero a restar (float a)
 * \param el segundo numero a restar (float b)
 * \return resultado de la resta de ambos numeros
 *
 */

float Resta (float a, float b);


/** \brief division de numero a, sobre numero b
 *
 * \param primer numero a ingresar (float a)
 * \param segundo numero a ingresar (float b)
 * \return resultado de division
 *
 */

float Division (float a, float b);

/** \brief multiplicacion de numero a, por numero b
 *
 * \param primer numero a ingresar (float a)
 * \param segundo numero a ingresar (float b)
 * \return resultado de la multiplicacion
 *
 */

float Multiplicacion (float a, float b);

/** \brief el factorial de numero a
 *
 * \param solo ingresamos el primer numero que nos pide (float a)
 * \param
 * \return el resultado de el factorial del numero "a"
 *
 */

float Factorial (float a);

